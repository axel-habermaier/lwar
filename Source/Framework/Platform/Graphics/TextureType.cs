using System;

namespace Pegasus.Framework.Platform.Graphics
{
	/// <summary>
	///   Describes the type of a texture.
	/// </summary>
	public enum TextureType
	{
		/// <summary>
		///   Indicates that a texture is two-dimensional.
		/// </summary>
		Texture2D = 2901,

		/// <summary>
		///   Indicates that a texture is a cube map.
		/// </summary>
		CubeMap = 2902
	}
}