using System;

namespace Pegasus.Platform.Graphics
{
	/// <summary>
	///   Specifies the size of the indices in an index buffer.
	/// </summary>
	public enum IndexSize
	{
		/// <summary>
		///   Indicates that the indices are 16 bit values.
		/// </summary>
		SixteenBits = 1601,

		/// <summary>
		///   Indicates that the indices are 32 bit values.
		/// </summary>
		ThirtyTwoBits = 1602
	}
}