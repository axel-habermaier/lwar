#pragma once

namespace OpenGL3
{
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// Buffer
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct Buffer
	{
		uint32 Obj;
		uint32 Type = 0;
		int32 Size = 0;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// BlendState
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct BlendState
	{
		BlendDescription Desc;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// DepthStencilState
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct DepthStencilState
	{
		DepthStencilDescription Desc;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// RasterizerState
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct RasterizerState
	{
		RasterizerDescription Desc;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// SamplerState
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct SamplerState
	{
		uint32 Obj;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// Query
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct Query
	{
		QueryType Type;
		uint32 Obj = 0;
		void* Sync = nullptr;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// Shader
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct Shader
	{
		uint32 Obj;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// ShaderProgram
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct ShaderProgram
	{
		uint32 Obj;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// RenderTarget
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct SwapChain;

	struct RenderTarget
	{
		uint32 Obj = 0;
		int32 Width = 0;
		int32 Height = 0;
		SwapChain* SwapChain = nullptr;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// SwapChain
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct SwapChain
	{
		SDL_Window* Window = nullptr;
		RenderTarget BackBuffer;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// Texture
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct Texture
	{
		uint32 Obj = 0;
		uint32 Format = 0;
		uint32 Type = 0;
	};

	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	// VertexLayout
	//-------------------------------------------------------------------------------------------------------------------------------------------------------
	struct VertexLayout
	{
		uint32 Obj = 0;
		uint32 IndexOffset = 0;
		uint32 IndexSize = 0;
		uint32 IndexType = 0;
	};
}